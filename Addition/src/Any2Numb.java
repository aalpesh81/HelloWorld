public class Any2Numb {
    public static void main(String[] args) {

        int a=10;
        int b=5;

        switch (1){
            case 1:
                System.out.println(a+b);
                break;
            case 2:
                System.out.println(a*b);
                break;
            case 3:
                System.out.println(a-b);
                break;
            case 4:
                System.out.println(a/b);
                break;
            default:
                System.out.println("Invalid Entry");
        }
    }
}


